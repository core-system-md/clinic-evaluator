/**
 * Clinic Evaluator — app.js
 * Constitution v4.0 Compliant
 * All text loaded from report_texts.json — zero hardcoded strings
 */

class ClinicEvaluatorApp {
  constructor() {
    this.config = null;
    this.texts = null;
    this.currentAssessmentKey = null;
    this.answers = {};
    this.metadata = { specialty: '', years: '', team: '' };
    this.engine = null;
  }

  async init() {
    try {
      await Promise.all([this.loadConfig(), this.loadTexts()]);
      this.setupEventListeners();
      if (window.preSelectedAssessment) {
        this.selectAssessment(window.preSelectedAssessment);
      }
    } catch (err) {
      console.error('Init failed:', err);
      this.showError(this.t('errors.init_failed'));
    }
  }

  async loadConfig() {
    const res = await fetch('config.json');
    if (!res.ok) throw new Error('config.json not found');
    this.config = await res.json();
  }

  async loadTexts() {
    const res = await fetch('report_texts.json');
    if (!res.ok) throw new Error('report_texts.json not found');
    this.texts = await res.json();
  }

  t(path, vars = {}) {
    const keys = path.split('.');
    let val = this.texts;
    for (const k of keys) {
      val = val?.[k];
      if (val === undefined) return path;
    }
    if (typeof val !== 'string') return val;
    return val.replace(/\{(\w+)\}/g, (_, key) => vars[key] ?? `{${key}}`);
  }

  setupEventListeners() {
    ['specialty', 'years', 'team'].forEach(f => {
      const el = document.getElementById(`meta-${f}`);
      if (el) el.addEventListener('input', e => { this.metadata[f] = e.target.value.trim(); });
    });
  }

  selectAssessment(key) {
    this.currentAssessmentKey = key;
    this.answers = {};
    const assessment = this.config.assessment_types?.[key];
    if (!assessment) { this.showError(this.t('errors.assessment_not_found')); return; }

    this.renderHeader(assessment);
    this.renderMetadata();
    this.renderProgress(assessment);
    this.renderQuestions(assessment);
    this.renderActions(assessment);
    this.toggleSimulator(assessment);
    this.scrollToTop();
  }

  renderHeader(a) {
    const t = document.getElementById('assessment-title');
    const s = document.getElementById('assessment-subtitle');
    if (t) t.textContent = a.title || '';
    if (s) s.textContent = a.subtitle || '';
  }

  renderMetadata() {
    const el = document.getElementById('metadata-form');
    if (el) el.style.display = 'block';
    const labels = {
      specialty: this.t('ui.metadata.specialty_label'),
      years: this.t('ui.metadata.years_label'),
      team: this.t('ui.metadata.team_label')
    };
    const placeholders = {
      specialty: this.t('ui.metadata.specialty_placeholder'),
      years: this.t('ui.metadata.years_placeholder'),
      team: this.t('ui.metadata.team_placeholder')
    };
    Object.entries(labels).forEach(([f, lbl]) => {
      const labelEl = document.querySelector(`label[for="meta-${f}"]`);
      if (labelEl) labelEl.textContent = lbl;
      const inputEl = document.getElementById(`meta-${f}`);
      if (inputEl) inputEl.placeholder = placeholders[f] || '';
    });
  }

  renderProgress(a) {
    const bar = document.getElementById('progress-bar');
    const txt = document.getElementById('progress-text');
    if (bar) bar.style.width = '0%';
    if (txt) txt.textContent = `0 ${this.t('ui.progress.of')} ${a.questions.length}`;
  }

  renderQuestions(assessment) {
    const container = document.getElementById('questions-container');
    if (!container) return;
    container.innerHTML = '';
    assessment.questions.forEach((q, idx) => {
      container.appendChild(this.buildQuestionCard(q, idx + 1));
    });
  }

  buildQuestionCard(q, num) {
    const card = document.createElement('div');
    card.className = 'question-card';
    card.dataset.qid = q.id;

    const header = document.createElement('div');
    header.className = 'q-header';

    const number = document.createElement('span');
    number.className = 'q-number';
    number.textContent = num;

    const text = document.createElement('h3');
    text.className = 'q-text';
    text.textContent = q.text;

    const meta = document.createElement('div');
    meta.className = 'q-meta';

    const impact = document.createElement('span');
    impact.className = `badge impact-${q.impact}`;
    impact.textContent = this.t(`ui.badges.impact_${q.impact}`);

    meta.appendChild(impact);

    if (q.layer === 'B') {
      const layer = document.createElement('span');
      layer.className = 'badge layer-B';
      layer.textContent = this.t('ui.badges.layer_trap');
      meta.appendChild(layer);
    }

    header.appendChild(number);
    header.appendChild(text);
    header.appendChild(meta);
    card.appendChild(header);

    const optsWrap = document.createElement('div');
    optsWrap.className = 'options-grid';

    q.options.forEach((opt) => {
      const lbl = document.createElement('label');
      lbl.className = 'option-card';
      lbl.dataset.value = opt.value;

      const inp = document.createElement('input');
      inp.type = 'radio';
      inp.name = `q-${q.id}`;
      inp.value = opt.value;
      inp.dataset.qid = q.id;
      inp.addEventListener('change', () => {
        this.answers[q.id] = parseInt(opt.value);
        this.highlightSelected(lbl, optsWrap);
        this.updateProgress();
      });

      const txt = document.createElement('span');
      txt.className = 'opt-text';
      txt.textContent = opt.label;

      const val = document.createElement('span');
      val.className = 'opt-val';
      val.textContent = opt.value;

      lbl.appendChild(inp);
      lbl.appendChild(txt);
      lbl.appendChild(val);
      optsWrap.appendChild(lbl);
    });

    card.appendChild(optsWrap);
    return card;
  }

  highlightSelected(activeLabel, container) {
    container.querySelectorAll('.option-card').forEach(l => l.classList.remove('selected'));
    activeLabel.classList.add('selected');
  }

  updateProgress() {
    const a = this.config.assessment_types[this.currentAssessmentKey];
    const total = a.questions.length;
    const done = Object.keys(this.answers).length;
    const pct = Math.round((done / total) * 100);

    const bar = document.getElementById('progress-bar');
    const txt = document.getElementById('progress-text');
    if (bar) bar.style.width = `${pct}%`;
    if (txt) txt.textContent = `${done} ${this.t('ui.progress.of')} ${total}`;
  }

  renderActions(a) {
    const box = document.getElementById('actions-container');
    if (!box) return;
    box.innerHTML = '';

    const btn = document.createElement('button');
    btn.className = 'btn-primary';
    btn.textContent = this.t('ui.buttons.submit');
    btn.addEventListener('click', () => this.handleSubmit());
    box.appendChild(btn);
  }

  toggleSimulator(a) {
    const sim = document.getElementById('ev-simulator');
    if (!sim) return;
    if (a.has_ev_simulator) {
      sim.style.display = 'block';
      const titleEl = sim.querySelector('h3');
      if (titleEl) titleEl.textContent = this.t('ui.simulator.title');
      const descEl = sim.querySelector('p');
      if (descEl) descEl.textContent = this.t('ui.simulator.description');
      const flowLabel = document.querySelector('label[for="sim-flow"]');
      if (flowLabel) flowLabel.textContent = this.t('ui.simulator.flow_label');
      const ltvLabel = document.querySelector('label[for="sim-ltv"]');
      if (ltvLabel) ltvLabel.textContent = this.t('ui.simulator.ltv_label');
    } else {
      sim.style.display = 'none';
    }
  }

  collectMetadata() {
    ['specialty', 'years', 'team'].forEach(f => {
      const el = document.getElementById(`meta-${f}`);
      this.metadata[f] = el ? el.value.trim() : '';
    });
    return this.metadata;
  }

  validate() {
    const a = this.config.assessment_types[this.currentAssessmentKey];
    const missing = a.questions.filter(q => this.answers[q.id] === undefined).map(q => q.id);

    if (missing.length > 0) {
      missing.forEach(qid => {
        const card = document.querySelector(`[data-qid="${qid}"]`);
        if (card) {
          card.classList.add('shake');
          setTimeout(() => card.classList.remove('shake'), 600);
        }
      });
      this.showError(this.t('errors.missing_answers', { count: missing.length }));
      return false;
    }
    return true;
  }

  async handleSubmit() {
    if (!this.validate()) return;

    const meta = this.collectMetadata();

    const btn = document.querySelector('.btn-primary');
    if (btn) { btn.disabled = true; btn.textContent = this.t('ui.buttons.processing'); }

    try {
      this.engine = new AssessmentEngine(this.config);
      const results = this.engine.evaluate(this.answers, this.currentAssessmentKey, meta);
      this.renderResults(results);
    } catch (err) {
      console.error(err);
      this.showError(this.t('errors.processing_error'));
    } finally {
      if (btn) { btn.disabled = false; btn.textContent = this.t('ui.buttons.submit'); }
    }
  }

  renderResults(res) {
    document.getElementById('assessment-form').style.display = 'none';
    document.getElementById('metadata-form').style.display = 'none';
    document.getElementById('progress-container').style.display = 'none';
    const ev = document.getElementById('ev-simulator');
    if (ev) ev.style.display = 'none';

    const out = document.getElementById('results-container');
    out.style.display = 'block';
    out.innerHTML = '';

    // Classification banner — Constitution v4.0 Article 5
    const q = res.classification || 'Q2';
    const qData = this.texts.quartiles[q] || this.texts.quartiles.Q2;
    const qLabel = qData.label;
    const qColor = qData.color;

    const banner = document.createElement('div');
    banner.className = 'result-banner';
    banner.style.borderColor = qColor;
    banner.innerHTML = `
      <div class="quartile-ring" style="border-color:${qColor};color:${qColor}">
        <span class="quartile-big">${q}</span>
      </div>
      <div class="quartile-info">
        <h2>${qLabel}</h2>
        <div class="overall-score">
          <span class="score-big" style="color:${qColor}">${(res.overallScore ?? 0).toFixed(1)}</span>
          <span class="score-denom">/ 100</span>
        </div>
      </div>
    `;
    out.appendChild(banner);

    // KPIs — Constitution v4.0 Article 4
    if (res.kpis) {
      const kpiSec = document.createElement('div');
      kpiSec.className = 'result-section';
      kpiSec.innerHTML = `<h3>${this.t('sections.kpis')}</h3>`;
      const grid = document.createElement('div');
      grid.className = 'kpi-grid';
      Object.entries(res.kpis).forEach(([k, v]) => {
        const kpiInfo = this.texts.kpis[k] || { name: k, short_name: k };
        const card = document.createElement('div');
        card.className = 'kpi-card';
        card.innerHTML = `
          <div class="kpi-name">${kpiInfo.name}</div>
          <div class="kpi-short">${kpiInfo.short_name}</div>
          <div class="kpi-num">${v.toFixed(1)}</div>
        `;
        grid.appendChild(card);
      });
      kpiSec.appendChild(grid);
      out.appendChild(kpiSec);
    }

    // Axes
    if (res.axisScores) {
      const axSec = document.createElement('div');
      axSec.className = 'result-section';
      axSec.innerHTML = `<h3>${this.t('sections.axes')}</h3>`;
      const list = document.createElement('div');
      list.className = 'axis-list';
      const a = this.config.assessment_types[this.currentAssessmentKey];
      Object.entries(res.axisScores).forEach(([aid, score]) => {
        const axis = a.axes.find(x => x.id === aid);
        const row = document.createElement('div');
        row.className = 'axis-row';
        row.innerHTML = `
          <div class="axis-head">
            <span class="axis-title">${axis ? axis.name_ar : aid}</span>
            <span class="axis-w">${((axis?.weight || 0) * 100).toFixed(0)}%</span>
          </div>
          <div class="axis-track"><div class="axis-fill" style="width:${score}%"></div></div>
          <span class="axis-score">${score.toFixed(1)}</span>
        `;
        list.appendChild(row);
      });
      axSec.appendChild(list);
      out.appendChild(axSec);
    }

    // Gaps (formerly "traps") — Constitution v4.0 Article 7.4
    // Penalties merged into analysis, never displayed explicitly
    if (res.traps && res.traps.length) {
      const tSec = document.createElement('div');
      tSec.className = 'result-section';
      tSec.innerHTML = `<h3>${this.t('sections.gaps')}</h3>`;
      res.traps.forEach(t => {
        const card = document.createElement('div');
        card.className = 'trap-card';
        card.innerHTML = `
          <div class="trap-title">${t.name}</div>
          <p class="trap-msg">${t.message}</p>
        `;
        tSec.appendChild(card);
      });
      out.appendChild(tSec);
    }

    // EV Simulator results
    if (res.evSimulator) {
      this.renderEVResults(res.evSimulator);
    }

    // Actions
    const actions = document.createElement('div');
    actions.className = 'result-actions';
    actions.innerHTML = `
      <button class="btn-secondary" onclick="window.print()">${this.t('ui.buttons.print')}</button>
      <button class="btn-secondary" onclick="location.reload()">${this.t('ui.buttons.new_assessment')}</button>
    `;
    out.appendChild(actions);

    this.scrollToTop();
  }

  renderEVResults(ev) {
    const box = document.getElementById('ev-simulator-results');
    if (!box) return;
    box.innerHTML = `
      <div class="ev-box">
        <h4>${this.t('sections.ev_simulator')}</h4>
        <div class="ev-grid">
          <div class="ev-item">
            <span>${this.t('sections.ev_current')}</span>
            <strong>$${(ev.currentEV || 0).toLocaleString()}</strong>
          </div>
          <div class="ev-item">
            <span>${this.t('sections.ev_potential')}</span>
            <strong>$${(ev.potentialEV || 0).toLocaleString()}</strong>
          </div>
          <div class="ev-item highlight">
            <span>${this.t('sections.ev_gap')}</span>
            <strong>+$${(ev.gap || 0).toLocaleString()}</strong>
          </div>
        </div>
      </div>
    `;
  }

  scrollToTop() { window.scrollTo({ top: 0, behavior: 'smooth' }); }

  showError(msg) {
    const el = document.getElementById('error-container');
    if (!el) { alert(msg); return; }
    el.textContent = msg;
    el.style.display = 'block';
    setTimeout(() => el.style.display = 'none', 5000);
  }
}

// Bootstrap
document.addEventListener('DOMContentLoaded', () => {
  window.app = new ClinicEvaluatorApp();
  window.app.init();
});
