// ─── SUPABASE CLIENT ───
class SupabaseClient {
  constructor(url, key) {
    this.url = url;
    this.key = key;
    this.headers = {
      'apikey': key,
      'Authorization': `Bearer ${key}`,
      'Content-Type': 'application/json',
      'Prefer': 'return=representation'
    };
  }

  async insert(table, data) {
    const res = await fetch(`${this.url}/rest/v1/${table}`, {
      method: 'POST',
      headers: this.headers,
      body: JSON.stringify(data)
    });
    if (!res.ok) throw new Error(`Supabase insert failed: ${res.status} ${await res.text()}`);
    return await res.json();
  }

  async select(table, columns = '*', filters = {}) {
    let url = `${this.url}/rest/v1/${table}?select=${columns}`;
    for (const [key, val] of Object.entries(filters)) {
      url += `&${key}=eq.${encodeURIComponent(val)}`;
    }
    const res = await fetch(url, { headers: this.headers });
    if (!res.ok) throw new Error(`Supabase select failed: ${res.status}`);
    return await res.json();
  }

  async rpc(fn, params = {}) {
    const res = await fetch(`${this.url}/rest/v1/rpc/${fn}`, {
      method: 'POST',
      headers: this.headers,
      body: JSON.stringify(params)
    });
    if (!res.ok) throw new Error(`Supabase RPC failed: ${res.status} ${await res.text()}`);
    return await res.json();
  }
}

// ─── ASSESSMENT ENGINE ───
class AssessmentEngine {
  constructor(config, reportTexts) {
    this.config = config;
    this.reportTexts = reportTexts || {};
    this.db = new SupabaseClient(SUPABASE_URL, SUPABASE_KEY);
    this.currentAssessment = null;
    this.answers = new Map();
    this.sessionId = null;
    this.leadId = null;
    this.startTime = null;
  }

  // ============================================================
  // 1. loadAssessment
  // ============================================================
  loadAssessment(typeSlug) {
    if (this.config.assessment_types && this.config.assessment_types[typeSlug]) {
      this.currentAssessment = this.config.assessment_types[typeSlug];
    } else {
      this.currentAssessment = this.config;
    }
    this.validateConfig();
    this.answers.clear();
    this.startTime = Date.now();
    return this.currentAssessment;
  }

  // ============================================================
  // 2. Validation Layer — 12 فحص
  // ============================================================
  validateConfig() {
    const cfg = this.currentAssessment || this.config;
    const errors = [];
    const axes = cfg.axes || [];
    const questions = cfg.questions || [];
    const traps = cfg.traps || [];

    const validAxisIds = new Set(axes.map(a => a.id));
    const validQuestionIds = new Set(questions.map(q => q.id));
    const validImpacts = new Set(['high', 'medium', 'low']);
    const validLayers = new Set(['A', 'B']);
    const validValues = new Set([0, 40, 100]);

    for (const q of questions) {
      if (!validAxisIds.has(q.axis_id)) {
        errors.push(`[VALIDATION-01] INVALID_AXIS_ID: "${q.axis_id}" in question "${q.id}" not found in axes list.`);
      }
    }

    for (const q of questions) {
      if (!validImpacts.has(q.impact)) {
        errors.push(`[VALIDATION-02] INVALID_IMPACT: "${q.impact}" in question "${q.id}".`);
      }
    }

    for (const q of questions) {
      if (!validLayers.has(q.layer)) {
        errors.push(`[VALIDATION-03] INVALID_LAYER: "${q.layer}" in question "${q.id}".`);
      }
    }

    for (const q of questions) {
      if (q.options && Array.isArray(q.options)) {
        for (const opt of q.options) {
          if (!validValues.has(opt.value)) {
            errors.push(`[VALIDATION-04] INVALID_VALUE: ${opt.value} in question "${q.id}".`);
          }
        }
      }
    }

    for (const trap of traps) {
      if (!validQuestionIds.has(trap.question_id)) {
        errors.push(`[VALIDATION-05] INVALID_TRAP: trap references non-existent question "${trap.question_id}".`);
      }
    }

    for (const trap of traps) {
      if (!validQuestionIds.has(trap.validates)) {
        errors.push(`[VALIDATION-06] INVALID_TRAP: trap validates non-existent question "${trap.validates}".`);
      }
    }

    for (const trap of traps) {
      if (!validAxisIds.has(trap.target_axis)) {
        errors.push(`[VALIDATION-07] INVALID_TRAP_AXIS: "${trap.target_axis}" in trap not found in axes list.`);
      }
    }

    for (const q of questions) {
      if (q.layer === 'B') {
        if (!q.trap_for || !Array.isArray(q.trap_for) || q.trap_for.length === 0) {
          errors.push(`[VALIDATION-08] ORPHAN_TRAP: question "${q.id}" is Layer B but has no "trap_for".`);
        }
      }
    }

    for (const trap of traps) {
      const pb = trap.penalty_base;
      const pm = trap.penalty_max;
      if (typeof pb !== 'number' || pb < 0 || pb > 100 || typeof pm !== 'number' || pm < 0 || pm > 100) {
        errors.push(`[VALIDATION-09] INVALID_PENALTY_RANGE: penalty_base=${pb}, penalty_max=${pm}.`);
      }
      if (pb > pm) {
        errors.push(`[VALIDATION-09b] INVALID_PENALTY_RANGE: penalty_base (${pb}) > penalty_max (${pm}).`);
      }
    }

    if (cfg.simulator && cfg.simulator.enabled === true) {
      const vars = cfg.simulator.variables || [];
      for (const v of vars) {
        if (v.min === undefined || v.max === undefined || v.default === undefined || v.step === undefined) {
          errors.push(`[VALIDATION-10] INVALID_SIMULATOR: variable "${v.id || 'N/A'}" missing fields.`);
        }
        if (v.default < v.min || v.default > v.max) {
          errors.push(`[VALIDATION-10b] INVALID_SIMULATOR: variable "${v.id}" default out of range.`);
        }
      }
    }

    const questionIds = questions.map(q => q.id);
    const duplicates = questionIds.filter((item, index) => questionIds.indexOf(item) !== index);
    if (duplicates.length > 0) {
      errors.push(`[VALIDATION-11] DUPLICATE_QUESTION_ID: [${[...new Set(duplicates)].join(', ')}].`);
    }

    for (const axis of axes) {
      const axisQuestions = questions.filter(q => q.axis_id === axis.id && q.layer === 'A');
      if (axisQuestions.length === 0) {
        errors.push(`[VALIDATION-12] EMPTY_AXIS: axis "${axis.id}" has no Layer A questions.`);
      }
    }

    if (errors.length > 0) {
      console.error('[ENGINE] ╔══════════════════════════════════════════════════════════════╗');
      console.error('[ENGINE] ║  CONFIG VALIDATION FAILED                                  ║');
      console.error('[ENGINE] ╚══════════════════════════════════════════════════════════════╝');
      errors.forEach((e, i) => console.error(`[ENGINE] ${i + 1}. ${e}`));
      throw new Error(`Config validation failed with ${errors.length} error(s).`);
    }
    console.log('[ENGINE] ✅ Config validation passed — all 12 checks OK');
  }

  // ============================================================
  // 3. Raw Scores — Layer A only
  // ============================================================
  calculateRawScores(answers) {
    const cfg = this.currentAssessment || this.config;
    const rawScores = {};
    for (const axis of cfg.axes) {
      const axisQuestions = cfg.questions.filter(
        q => q.axis_id === axis.id && q.layer === 'A'
      );
      let earned = 0;
      let maxPossible = 0;
      for (const q of axisQuestions) {
        const answerValue = answers[q.id];
        if (answerValue === undefined || answerValue === null) continue;
        const multiplier = this.getMultiplier(q.impact);
        earned += answerValue * multiplier;
        maxPossible += 100 * multiplier;
      }
      rawScores[axis.id] = { earned, maxPossible };
    }
    return rawScores;
  }

  // ============================================================
  // 4. Dynamic Penalties
  // ============================================================
  applyPenalties(answers, rawScores) {
    const cfg = this.currentAssessment || this.config;
    const penalties = {};
    let totalPenalty = 0;
    for (const trap of cfg.traps || []) {
      const targetVal = answers[trap.validates];
      const validatorVal = answers[trap.question_id];
      if (targetVal === undefined || validatorVal === undefined) continue;
      if (targetVal !== 100) continue;
      const severity = Math.abs(targetVal - validatorVal) / 100;
      const penaltyPercent = trap.penalty_base + (
        severity * (trap.penalty_max - trap.penalty_base)
      );
      const axisEarned = rawScores[trap.target_axis].earned;
      const axisPenalty = axisEarned * (penaltyPercent / 100);
      penalties[trap.target_axis] = (penalties[trap.target_axis] || 0) + axisPenalty;
      totalPenalty += axisPenalty;
      console.log(`[PENALTY] Trap ${trap.question_id} → Axis ${trap.target_axis}: severity=${severity.toFixed(2)}, penalty=${penaltyPercent.toFixed(1)}%`);
    }
    return { penalties, totalPenalty };
  }

  // ============================================================
  // 5. Final Axis Scores
  // ============================================================
  calculateFinalScores(rawScores, penalties) {
    const cfg = this.currentAssessment || this.config;
    const finalScores = {};
    for (const axis of cfg.axes) {
      const raw = rawScores[axis.id];
      const penalty = penalties[axis.id] || 0;
      if (raw.maxPossible === 0) {
        finalScores[axis.id] = 0;
        continue;
      }
      let score = ((raw.earned - penalty) / raw.maxPossible) * 100;
      score = Math.max(0, Math.min(100, score));
      finalScores[axis.id] = Math.round(score);
    }
    return finalScores;
  }

  // ============================================================
  // 6. Global Score
  // ============================================================
  calculateGlobal(finalScores) {
    const cfg = this.currentAssessment || this.config;
    const totalWeight = cfg.axes.reduce((sum, axis) => sum + (axis.weight || 1.0), 0);
    const weightedSum = cfg.axes.reduce((sum, axis) => sum + (finalScores[axis.id] * (axis.weight || 1.0)), 0);
    const globalScore = weightedSum / totalWeight;
    const quartiles = {};
    for (const axis of cfg.axes) {
      quartiles[axis.id] = this.getQuartile(finalScores[axis.id]).q;
    }
    // Overall quartile
    const overallQ = this.getQuartile(globalScore).q;
    quartiles.overall = overallQ;
    return {
      globalScore: Math.round(globalScore),
      leakageIndex: Math.round(100 - globalScore),
      axisScores: finalScores,
      quartiles: quartiles,
      totalEarned: weightedSum,
      totalWeight: totalWeight
    };
  }

  // ============================================================
  // 7. KPIs — Constitution v4.0 Article 4
  // ============================================================
  calculateKPIs(finalScores) {
    const ids = Object.keys(finalScores);
    // Map by position if IDs are A1, A2... or axis_1, axis_2...
    const getScore = (id) => finalScores[id] || 0;
    
    // Find axis IDs
    const a1 = ids.find(id => id.includes('1')) || ids[0];
    const a2 = ids.find(id => id.includes('2')) || ids[1];
    const a3 = ids.find(id => id.includes('3')) || ids[2];
    const a4 = ids.find(id => id.includes('4')) || ids[3];
    const a5 = ids.find(id => id.includes('5')) || ids[4];

    return {
      TFI: Math.round((getScore(a1) + getScore(a2)) / 2),
      TAP: Math.round((getScore(a3) + getScore(a4)) / 2),
      PRP: Math.round(getScore(a5)),
      PLI: Math.round(getScore(a5))
    };
  }

  // ============================================================
  // 8. EV Simulator — Fixed to use config axis IDs
  // ============================================================
  calculateEV(axisScores, simulatorVars) {
    const cfg = this.currentAssessment || this.config;
    if (!cfg.simulator || cfg.simulator.enabled !== true) return null;
    
    const ids = Object.keys(axisScores);
    const a3 = ids.find(id => id.includes('3')) || ids[2];
    const a4 = ids.find(id => id.includes('4')) || ids[3];
    const a5 = ids.find(id => id.includes('5')) || ids[4];
    
    const deltaCMax = cfg.simulator.delta_c_max || 0.35;
    const flow = simulatorVars?.flow || 50;
    const ltv = simulatorVars?.ltv || 5000;
    
    if (deltaCMax <= 0 || flow <= 0 || ltv <= 0) return null;
    
    const weightedScore =
      (axisScores[a3] || 0) * 0.4 +
      (axisScores[a4] || 0) * 0.4 +
      (axisScores[a5] || 0) * 0.2;
    
    const deltaC = (weightedScore / 100) * deltaCMax;
    const EV_base = deltaC * flow * ltv;
    
    return {
      currentEV: Math.round(EV_base * 0.7),
      potentialEV: Math.round(EV_base * 1.25),
      gap: Math.round(EV_base * 0.55)
    };
  }

  // ============================================================
  // 9. Quartile Classification — Constitution v4.0 Article 5
  // ============================================================
  getQuartile(score) {
    if (score >= 75) return { q: 'Q4', label: 'أداء متميز', color: '#2A6F5D' };
    if (score >= 50) return { q: 'Q3', label: 'أداء مستقر', color: '#5C6B73' };
    if (score >= 25) return { q: 'Q2', label: 'تذبذب ملحوظ', color: '#C67D47' };
    return { q: 'Q1', label: 'فجوة هيكلية حرجة', color: '#A33B3B' };
  }

  // ============================================================
  // 10. Impact Multiplier
  // ============================================================
  getMultiplier(impact) {
    const multipliers = { high: 1.5, medium: 1.0, low: 0.5 };
    return multipliers[impact] || 1.0;
  }

  // ============================================================
  // 11. setAnswer / getAnswer
  // ============================================================
  setAnswer(questionId, value) {
    const cfg = this.currentAssessment || this.config;
    const q = cfg.questions.find(q => q.id === questionId);
    if (!q) throw new Error(`Question '${questionId}' not found`);
    const answer = {
      questionId: q.id,
      axisId: q.axis_id,
      questionText: q.text,
      value: parseInt(value),
      isTrap: q.layer === 'B',
      answeredAt: new Date().toISOString()
    };
    this.answers.set(questionId, answer);
    return answer;
  }

  getAnswer(questionId) { return this.answers.get(questionId) || null; }

  getProgress() {
    const cfg = this.currentAssessment || this.config;
    if (!cfg) return { current: 0, total: 0, percentage: 0 };
    const total = cfg.questions.length;
    const current = this.answers.size;
    return { current, total, percentage: Math.round((current / total) * 100), remaining: total - current };
  }

  // ============================================================
  // 12. detectTraps — COMPLETED
  // ============================================================
  detectTraps() {
    const answers = {};
    this.answers.forEach((ans, qid) => { answers[qid] = ans.value; });
    const rawScores = this.calculateRawScores(answers);
    const { penalties } = this.applyPenalties(answers, rawScores);
    const cfg = this.currentAssessment || this.config;
    const triggered = [];
    
    for (const trap of cfg.traps || []) {
      const targetVal = answers[trap.validates];
      const validatorVal = answers[trap.question_id];
      if (targetVal === undefined || validatorVal === undefined) continue;
      if (targetVal !== 100) continue;
      
      const severity = Math.abs(targetVal - validatorVal) / 100;
      const penaltyPercent = trap.penalty_base + (
        severity * (trap.penalty_max - trap.penalty_base)
      );
      
      triggered.push({
        name: trap.name,
        message: trap.message_ar || trap.message,
        penaltyApplied: Math.round(penaltyPercent)
      });
    }
    return triggered;
  }

  // ============================================================
  // 13. evaluate — MAIN METHOD called by app.js
  // ============================================================
  evaluate(answers, assessmentKey, metadata) {
    // Load assessment
    if (assessmentKey) {
      this.loadAssessment(assessmentKey);
    }
    
    // Set answers
    for (const [qid, val] of Object.entries(answers)) {
      this.setAnswer(qid, val);
    }
    
    // Calculate
    const rawScores = this.calculateRawScores(answers);
    const { penalties, totalPenalty } = this.applyPenalties(answers, rawScores);
    const finalScores = this.calculateFinalScores(rawScores, penalties);
    const globalResult = this.calculateGlobal(finalScores);
    
    // KPIs
    const kpis = this.calculateKPIs(finalScores);
    
    // Traps
    const traps = this.detectTraps();
    
    // EV Simulator
    const evSimulator = this.calculateEV(finalScores, metadata);
    
    return {
      classification: globalResult.quartiles.overall,
      overallScore: globalResult.globalScore,
      axisScores: finalScores,
      kpis: kpis,
      traps: traps,
      evSimulator: evSimulator
    };
  }

  // ============================================================
  // 14. calculateScores — wrapper for compatibility
  // ============================================================
  calculateScores() {
    const answers = {};
    this.answers.forEach((ans, qid) => { answers[qid] = ans.value; });
    const rawScores = this.calculateRawScores(answers);
    const { penalties } = this.applyPenalties(answers, rawScores);
    const finalScores = this.calculateFinalScores(rawScores, penalties);
    const globalResult = this.calculateGlobal(finalScores);
    const scores = [];
    const cfg = this.currentAssessment || this.config;
    for (const axis of cfg.axes) {
      scores.push({
        axisId: axis.id,
        axisNameAr: axis.name_ar,
        axisNameEn: axis.name_en,
        weight: axis.weight,
        percentage: finalScores[axis.id],
        grade: this.getQuartile(finalScores[axis.id]).q
      });
    }
    const overallQ = this.getQuartile(globalResult.globalScore).q;
    const overallLabel = this.getQuartile(globalResult.globalScore).label;
    return {
      axes: scores,
      overall: {
        percentage: globalResult.globalScore,
        grade: overallQ,
        title: overallLabel,
        body: `الدرجة الكلية: ${globalResult.globalScore}% — ${overallLabel}`
      }
    };
  }

  // ============================================================
  // 15. buildReport — legacy, kept for compatibility
  // ============================================================
  buildReport(answers, leadData = {}, simulatorVars = {}) {
    const rawScores = this.calculateRawScores(answers);
    const { penalties } = this.applyPenalties(answers, rawScores);
    const finalScores = this.calculateFinalScores(rawScores, penalties);
    const globalResult = this.calculateGlobal(finalScores);
    const evResult = this.calculateEV(finalScores, simulatorVars);

    const globalQ = this.getQuartile(globalResult.globalScore).q;
    const qData = this.getQuartile(globalResult.globalScore);

    return {
      _debug: { rawScores, penalties, finalScores, globalResult, evResult },
      global: {
        score: globalResult.globalScore,
        quartile: globalQ,
        label: qData.label,
        color: qData.color
      },
      leakage: {
        index: globalResult.leakageIndex
      },
      axes: finalScores,
      ev: evResult
    };
  }
}
