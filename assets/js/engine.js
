  static async loadJSON(url) {
    const response = await fetch(url);
    if (!response.ok) throw new Error(`Failed to load ${url}: HTTP ${response.status}`);
    return await response.json();
  }

  static async create(configUrl, reportTextsUrl) {
    const [config, reportTexts] = await Promise.all([
      AssessmentEngine.loadJSON(configUrl),
      AssessmentEngine.loadJSON(reportTextsUrl)
    ]);
    return new AssessmentEngine(config, reportTexts);
  }
