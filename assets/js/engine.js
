  async completeSession(sessionId, totalScore, percentage) {
    const res = await fetch(`${SUPABASE_URL}/rest/v1/sessions?id=eq.${sessionId}`, {
      method: 'PATCH',
      headers: { ...this.db.headers, 'Prefer': 'return=minimal' },
      body: JSON.stringify({ status: 'completed', completed_at: new Date().toISOString() })
    });
    if (!res.ok) throw new Error('Failed to complete session');
    const leadRes = await fetch(`${SUPABASE_URL}/rest/v1/leads?id=eq.${this.leadId}`, {
      method: 'PATCH',
      headers: { ...this.db.headers, 'Prefer': 'return=minimal' },
      body: JSON.stringify({ completed: true, score_total: totalScore, score_percentage: percentage, completed_at: new Date().toISOString() })
    });
    if (!leadRes.ok) throw new Error('Failed to update lead');
  }

